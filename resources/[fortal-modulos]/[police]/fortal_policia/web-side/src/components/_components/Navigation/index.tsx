import React, { useState, useEffect } from "react";

import { useLocation, useNavigate } from "react-router-dom";
import { usePermissions } from "@/providers/Permissions";

export const Navigation = () => {
  const [selected, setSelected] = useState(0);
  const location = useLocation();
  const navigate = useNavigate();
  const { isLoading, canAccessPage } = usePermissions();

  // Atualizar selected baseado na rota atual
  useEffect(() => {
    const path = location.pathname;
    
    const buttonIndex = button.findIndex(btn => btn.url === path);
    if (buttonIndex !== -1) {
      setSelected(buttonIndex);
    }
  }, [location.pathname]);

  const button = [
    {
      icon: <Icon.home />,
      url: "/panel",
      pageName: "home"
    },
    {
      icon: <Icon.search />,
      url: "/panel/search",
      pageName: "search"
    },
    {
      icon: <Icon.locked />,
      url: "/panel/locked",
      pageName: "locked"
    },
    {
      icon: <Icon.warn />,
      url: "/panel/fine",
      pageName: "fine"
    },
    {
      icon: <Icon.star />,
      url: "/panel/wanted",
      pageName: "wanted"
    },
    {
      icon: <Icon.list />,
      url: "/panel/occurrence",
      pageName: "occurrence"
    },
    {
      icon: <Icon.connect />,
      url: "/panel/members",
      pageName: "members"
    },
  ];

  const handleCloseNUI = () => {
    
    // Enviar mensagem para o cliente fechar a NUI
    if (window.invokeNative) {
      fetch('https://fortal_police/closeNUI', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({}),
      }).then(() => {}).catch((error) => {
        console.error('[NAVIGATION] Erro ao fechar NUI:', error);
      });
    }
  };

  const handleButtonClick = (url: string, index: number) => {
    setSelected(index);
    navigate(url);
  };

  // Se ainda está carregando, mostrar loading
  if (isLoading) {
    return (
      <div className="flex h-full w-[5vw] flex-col items-center justify-between border border-[#FFFFFF08] bg-[#FFFFFF01] py-[1.2vw]">
        <div className="flex h-[3.2vw] w-[3.2vw] items-center justify-center rounded-full border border-[#FFFFFF1A] p-[.2vw]">
          <img src="./logo.png" className="h-full w-full rounded-full" alt="" />
        </div>
        <div className="flex flex-col gap-[.5vw]">
          <div className="h-[2.5vw] w-[2.5vw] animate-pulse bg-white/10 rounded-[.5vw]"></div>
        </div>
        <button 
          onClick={handleCloseNUI}
          className="flex w-[2.7vw] flex-col items-center gap-[.3vw] rounded-[.5vw] py-[.6svw] text-white/80 hover:text-white"
        >
          <Icon.esq />
        </button>
      </div>
    );
  }

  return (
    <div className="flex h-full w-[5vw] flex-col items-center justify-between border border-[#FFFFFF08] bg-[#FFFFFF01] py-[1.2vw]">
      <div className="flex h-[3.2vw] w-[3.2vw] items-center justify-center rounded-full border border-[#FFFFFF1A] p-[.2vw]">
        <img src="./logo.png" className="h-full w-full rounded-full" alt="" />
      </div>
      <div className="flex flex-col gap-[.5vw]">
        {button.map((data, i) => {
          // Verificar se o jogador pode acessar a página
          const canAccess = canAccessPage(data.pageName);
         
          if (canAccess) {
            return (
              <button
                key={i}
                onClick={() => handleButtonClick(data.url, i)}
                className={`${
                  selected === i ? "text-white" : "text-white/20"
                } flex h-[2.5vw] w-[2.5vw] items-center justify-center rounded-[.5vw]`}
              >
                <span
                  className={`${selected === i ? "text-white" : "text-white/10"}`}
                >
                  {data.icon}
                </span>
              </button>
            );
          }
        
          return null;
        })}
      </div>
      <button 
        onClick={handleCloseNUI}
        className="flex w-[2.7vw] flex-col items-center gap-[.3vw] rounded-[.5vw] py-[.6svw] text-white/80 hover:text-white"
      >
        <Icon.esq />
      </button>
    </div>
  );
};

