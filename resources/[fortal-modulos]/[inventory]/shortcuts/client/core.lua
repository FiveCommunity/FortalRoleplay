-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp", "lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
vSERVER = Tunnel.getInterface("shortcuts")
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local Shortcuts = false
-----------------------------------------------------------------------------------------------------------------------------------------
-- SHORTSHOW
-----------------------------------------------------------------------------------------------------------------------------------------
function Shortshow()
	if exports["coreP"].handCuff() then 
		return
	end

	if not Shortcuts then
		SendNUIMessage({ action = "Open", payload = vSERVER.Shortcuts() })
		Shortcuts = true
	end
end

-----------------------------------------------------------------------------------------------------------------------------------------
-- SHORTCLOSE
-----------------------------------------------------------------------------------------------------------------------------------------
function Shortclose()
	SendNUIMessage({ action = "Close" })
	Shortcuts = false
end

-----------------------------------------------------------------------------------------------------------------------------------------
-- HIDESHORTCUTS
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterCommand("+shortcuts", Shortshow)
RegisterCommand("-shortcuts", Shortclose)
RegisterKeyMapping("+shortcuts", "Visualizar atalhos r√°pidos.", "keyboard", "TAB")
